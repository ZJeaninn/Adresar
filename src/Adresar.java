    /**
     * @param args the command line arguments
     */
    public ArrayList<Oseba> Tabela() {
        ArrayList<Oseba> Tabela1 = new ArrayList<>();

        String data = "jdbc:derby://localhost:1527/Oseba";
        try (
                Connection conn = DriverManager.getConnection(
                        data);
                Statement st = conn.createStatement()) {

                    Class.forName("org.apache.derby.jdbc.ClientDriver");

                    String query1 = "Select * FROM Oseba";
                    ResultSet rs = st.executeQuery(query1);
                    Oseba oseba;
                    while (rs.next()) {
                        oseba = new Oseba(rs.getInt("IDOSEBA"), rs.getString("PRIIMEK"), rs.getString("STEVILKA"), rs.getInt("POSTA"));
                        Tabela1.add(oseba);
                    }

                } catch (SQLException s) {
                    System.out.println("SQL Error: " + s.toString() + " "
                            + s.getErrorCode() + " " + s.getSQLState());
                } catch (Exception e) {
                    System.out.println("Error: " + e.toString()
                            + e.getMessage());
                }
                return Tabela1;
    }

    private void Prikazi() {
        ArrayList<Oseba> list = Tabela();
        DefaultTableModel model = (DefaultTableModel) TabelaOseb.getModel();
       // poišči za brisanje tabele
        Object[] row = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getPriimek();
            row[2] = list.get(i).getStevilka();
            row[3] = list.get(i).getPosta();
            model.addRow(row);

        }

        model.fireTableDataChanged();
    }

